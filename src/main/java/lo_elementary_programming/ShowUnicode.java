package lo_elementary_programming;

public class ShowUnicode {

    public static void main(String[] args) {

        System.out.println("\u2622");  // radioactive sign
        System.out.println("\u263A");  // white smiling face sign
        System.out.println("\u263b");  // black smiling sign

        System.out.println("\u2680");  // die face 1 sign
        System.out.println("\u2681");  // die face 2 sign
        System.out.println("\u2682");  // die face 3 sign
        System.out.println("\u2683");  // die face 4 sign
        System.out.println("\u2684");  // die face 5 sign
        System.out.println("\u2685");  // die face 6 sign
 
    }

}
